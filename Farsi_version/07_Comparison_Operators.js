let result;
let num1 = 5;
let num2 = 10;

// ===============================
// 🎓 عملگرهای مقایسه‌ای در JavaScript
// 📌 خروجی همیشه true یا false است (نوع Boolean)
// 📌 کاربرد: بیشتر در شرط‌ها (if, while, for) برای تصمیم‌گیری
// ===============================

// --------- کوچکتر (<) ---------
// بررسی می‌کند آیا num1 از num2 کوچکتر است؟
result = num1 < num2;   // 5 < 10 → true
console.log(result);

// --------- بزرگتر (>) ---------
// بررسی می‌کند آیا num1 از num2 بزرگتر است؟
result = num1 > num2;   // 5 > 10 → false
console.log(result);

// --------- کوچکتر یا مساوی (<=) ---------
result = num1 <= num2;  // 5 <= 10 → true
console.log(result);

// --------- بزرگتر یا مساوی (>=) ---------
result = num1 >= num2;  // 5 >= 10 → false
console.log(result);

// --------- مساوی (==) ---------
// 📌 مقایسه فقط مقدار (نه نوع)
// مثال: 5 == "5" → true
result = num1 == num2;  
console.log(result);

// --------- مساوی سختگیرانه (===) ---------
// 📌 مقایسه مقدار + نوع داده
// مثال: 5 === "5" → false
result = num1 === num2; 
console.log(result);

// --------- نابرابر (!=) ---------
// 📌 بررسی می‌کند آیا مقدارها برابر نیستند (نوع داده مهم نیست)
result = num1 != num2;  
console.log(result);

// --------- نابرابر سختگیرانه (!==) ---------
// 📌 بررسی می‌کند آیا مقدار یا نوع داده متفاوت است
result = num1 !== num2; 
console.log(result);

// ✨ نکات مهم

// == و != نوع داده رو در نظر نمی‌گیرن → به همین دلیل توصیه نمی‌شن.

// همیشه بهتره از === و !== استفاده کنی تا هم مقدار و هم نوع چک بشه.

// خروجی همه این عملگرها true یا false هست و بیشتر توی شرط‌ها (if, while, for) کاربرد دارن.